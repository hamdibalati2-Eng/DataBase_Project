    private void setupTableModel() {
    // Define column names
    String[] columnNames = {"ID", "Title", "Description", "Date"};
    
    // Create model with columns
    tableModel = new DefaultTableModel(columnNames, 0) {
        @Override
        public Class<?> getColumnClass(int columnIndex) {
            if (columnIndex == 0) return Integer.class;
            if (columnIndex == 3) return java.sql.Timestamp.class;
            return String.class;
        }
        
        @Override
        public boolean isCellEditable(int row, int column) {
            return false; // Make all cells non-editable
        }
    };
    
    // Set the model to the table
    achievementtable.setModel(tableModel);
    
    // Optional: Hide the ID column if needed
    achievementtable.getColumnModel().getColumn(0).setMinWidth(50);
    achievementtable.getColumnModel().getColumn(0).setPreferredWidth(50);
    
    // Debug
    System.out.println("Table model initialized with columns: " + 
                       String.join(", ", columnNames));
}
    private String getCurrentTimestampString() {
    java.sql.Timestamp timestamp = new java.sql.Timestamp(System.currentTimeMillis());
    java.text.SimpleDateFormat dateFormat = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
    return dateFormat.format(timestamp);
}
    private void setupRowSelectionListener() {
    // Create a mouse listener directly without casting
    achievementtable.addMouseListener(new java.awt.event.MouseAdapter() {
        @Override
        public void mouseClicked(java.awt.event.MouseEvent e) {
            int selectedRow = achievementtable.getSelectedRow();
            if (selectedRow != -1) {
                try {
                    // Use full qualification for all types
                    selectedRowId = (int) tableModel.getValueAt(selectedRow, 0);
                    jTextField1.setText(tableModel.getValueAt(selectedRow, 1).toString()); // Title
                    jTextField2.setText(tableModel.getValueAt(selectedRow, 2).toString()); // Description
                    
                    // Format the date for display
                    java.sql.Timestamp timestamp = (java.sql.Timestamp) tableModel.getValueAt(selectedRow, 3);
                    java.text.SimpleDateFormat dateFormat = new java.text.SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
                    jTextField3.setText(dateFormat.format(timestamp));
                } catch (Exception ex) {
                    System.err.println("Error selecting row: " + ex.getMessage());
                    ex.printStackTrace();
                }
            }
        }
    });
}
