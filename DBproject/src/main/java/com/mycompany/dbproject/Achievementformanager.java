    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        String title = jTextField1.getText().trim();
    String description = jTextField2.getText().trim();
    String dateStr = jTextField3.getText().trim();
    
    if (title.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Title cannot be empty", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        // Parse the date string to a timestamp
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        java.util.Date parsedDate = dateFormat.parse(dateStr);
        // Properly convert util.Date to sql.Timestamp
        java.sql.Timestamp timestamp = new java.sql.Timestamp(parsedDate.getTime());
        
        boolean success = Services.addAchievement(title, description, timestamp);
        
        if (success) {
            JOptionPane.showMessageDialog(this, "Achievement added successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            clearFields();
            loadAchievements(); // Refresh table
        } else {
            JOptionPane.showMessageDialog(this, "Failed to add achievement", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(this, "Invalid date format. Use yyyy-MM-dd HH:mm:ss", "Validation Error", JOptionPane.ERROR_MESSAGE);
    }
    }//GEN-LAST:event_jButton1ActionPerformed

    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        if (selectedRowId == -1) {
        JOptionPane.showMessageDialog(this, "Please select an achievement first", "Selection Required", JOptionPane.WARNING_MESSAGE);
        return;
    }
    
    String title = jTextField1.getText().trim();
    String description = jTextField2.getText().trim();
    String dateStr = jTextField3.getText().trim();
    
    if (title.isEmpty()) {
        JOptionPane.showMessageDialog(this, "Title cannot be empty", "Validation Error", JOptionPane.ERROR_MESSAGE);
        return;
    }
    
    try {
        // Parse the date string to a timestamp - remove the unnecessary casting
        SimpleDateFormat dateFormat = new SimpleDateFormat("yyyy-MM-dd HH:mm:ss");
        java.util.Date parsedDate = dateFormat.parse(dateStr);
        java.sql.Timestamp timestamp = new java.sql.Timestamp(parsedDate.getTime());
        
        // Debug information
        System.out.println("Updating achievement ID: " + selectedRowId);
        System.out.println("Title: " + title);
        System.out.println("Description: " + description);
        System.out.println("Date: " + timestamp);
        
        boolean success = Services.updateAchievement(selectedRowId, title, description, timestamp);
        
        if (success) {
            JOptionPane.showMessageDialog(this, "Achievement updated successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
            loadAchievements(); // Refresh table
        } else {
            JOptionPane.showMessageDialog(this, "Failed to update achievement", "Error", JOptionPane.ERROR_MESSAGE);
        }
    } catch (ParseException e) {
        JOptionPane.showMessageDialog(this, "Invalid date format. Use yyyy-MM-dd HH:mm:ss", "Validation Error", JOptionPane.ERROR_MESSAGE);
        e.printStackTrace(); // Print stack trace for debugging
    }
    }//GEN-LAST:event_jButton2ActionPerformed

    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
         if (selectedRowId == -1) {
            JOptionPane.showMessageDialog(this, "Please select an achievement first", "Selection Required", JOptionPane.WARNING_MESSAGE);
            return;
        }
        
        int confirm = JOptionPane.showConfirmDialog(this, 
            "Are you sure you want to delete this achievement?", 
            "Confirm Deletion", 
            JOptionPane.YES_NO_OPTION,
            JOptionPane.WARNING_MESSAGE);
        
        if (confirm == JOptionPane.YES_OPTION) {
            boolean success = Services.deleteAchievement(selectedRowId);
            
            if (success) {
                JOptionPane.showMessageDialog(this, "Achievement deleted successfully", "Success", JOptionPane.INFORMATION_MESSAGE);
                clearFields();
                loadAchievements(); // Refresh table
                selectedRowId = -1; // Reset selection
            } else {
                JOptionPane.showMessageDialog(this, "Failed to delete achievement", "Error", JOptionPane.ERROR_MESSAGE);
            }
        }
    }//GEN-LAST:event_jButton3ActionPerformed


